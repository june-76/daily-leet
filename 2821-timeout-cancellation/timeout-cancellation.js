/**
 * @param {Function} fn
 * @param {Array} args
 * @param {number} t
 * @return {Function}
 */
var cancellable = function(fn, args, t) {
    // 캔슬 여부를 판단할 변수(플래그)
    let cancelled = false;

    // 캔슬을 true로 설정
    const cancelFn = () => {
        cancelled = true;
    };

    // 함수 지연 실행
    setTimeout(() => {
        if (!cancelled) {
            fn(...args);
        }
    }, t);

    // cancelFn() 함수 실행
    return cancelFn;
};

